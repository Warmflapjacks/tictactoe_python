#Nested list as a board
board = [['-', '-', '-'],
         ['-', '-', '-'],
         ['-', '-', '-'] ]

#Display board
print(board[0])
print(board[1])
print(board[2])

#X User input
col = int(input("X player, select a column 1-3: "))
row = int(input("X player, select a row 1-3: "))
col -= 1
row -= 1

if (board[row][col] == '-'):
    board[row][col] = 'X'
else:
    print("This spot on the board is already taken")

#Update board
print(board[0])
print(board[1])
print(board[2])

